package com.zifang.util.http;

class Data {
    String name;
    String pw;

    /**
     * getName方法。
     *
     * @return String类型返回值
     */
    public String getName() {
        return name;
    }

    /**
     * setName方法。
     * * @param name String类型参数
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * getPw方法。
     *
     * @return String类型返回值
     */
    public String getPw() {
        return pw;
    }

    /**
     * setPw方法。
     * * @param pw String类型参数
     */
    public void setPw(String pw) {
        this.pw = pw;
    }
}
